struct Trie {
	vector<vector<int>> to;
	vector<int> end, pref;
	int sigma; char norm;
	Trie(int sigma_=26, char norm_='a') : sigma(sigma_), norm(norm_) {
		to = {vector<int>(sigma)};
		end = {0}, pref = {0};
	}
	void insert(string s) {
		int x = 0;
		for (auto c : s) {
			int &nxt = to[x][c-norm];
			if (!nxt) {
				nxt = to.size();
				to.push_back(vector<int>(sigma));
				end.push_back(0), pref.push_back(0);
			}
			x = nxt, pref[x]++;
		}
		end[x]++, pref[0]++;
	}
	void erase(string s) {
		int x = 0;
		for (char c : s) {
			int &nxt = to[x][c-norm];
			x = nxt, pref[x]--;
			if (!pref[x]) nxt = 0;
		}
		end[x]--, pref[0]--;
	}
	int find(string s) {
		int x = 0;
		for (auto c : s) {
			x = to[x][c-norm];
			if (!x) return -1;
		}
		return x;
	}
	int count_pref(string s) {
		int id = find(s);
		return id >= 0 ? pref[id] : 0;
	}

    bool has_prefix(const string& s){
        int x = 0;
        for(char c : s){
            if(end[x]) return true;
            x = to[x][c - norm];
            if(!x) return false;
        }
        return end[x];
    }
};

void solve()
{
    int q; cin >> q;
    Trie t;
    vector<string> s;
    vector<bool> vis;
    vi ativo;
    int cnt = 0;
    while(q--){
        int op; cin >> op;
        string st; cin >> st;
        if(op == 1){
            t.insert(st);
            vi novo;
            for(auto i : ativo){
                if(!t.has_prefix(s[i])){
                    novo.pb(i);
                }else{
                    cnt--;
                    vis[i] = false;
                }
            }
            ativo = move(novo);
        }
        else {
            s.pb(st);
            vis.pb(!t.has_prefix(st));
            if(vis.back()){
                ativo.pb(sz(s)-1);
                cnt++;
            }
        }
        cout << cnt << endl;
    }
}

